        <h3>Tentang Buku </h3>
        <p>Buku fiksi, merupakan buku yang diciptakan penulis sesuai dengan imajinasinya, bukan merupakan bukudengan cerita yang tidak nyata, namun pembaca merasa seolah-olah cerita tersebut nyata. Jenis buku ini adalahkumpulan cerita pendek, kumpulan puisi, kumpulan drama, serta novel.
          </p>
        <p>Buku nonfiksi, merupakan buku berdasarkan data valid tentang pengetahuan tanpa mengurangi isi data tersebut. Jenis buku ini adalah buku referensi, buku petunjuk/panduan, buku pelajaran, kamus, ensiklopedia, directory, peta.
          </p>
        <p>Novel, merupakan karya fiksi prosa yang tertulis dan narasi, biasanya dalam bentuk cerita. Novel biasanya berisi kurang lebih 4.000 kata, lebih kompleks dari cerpen, dan tidak dibatasi keterbatasan struktural dan sajak.</p>
        
